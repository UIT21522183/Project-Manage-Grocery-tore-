    private void showInvoice(Invoice result) {
        String status="unpaid";
        if(result.isPayment_status()==true) status ="paid";
  var row = new Object[] {
       result.getInvoice_id(), result.getCustomer_id(), result.getCreated_at(),
      result.getTotal_amount(), result.getPayment_method(), status,
      result.getEmployee_id()
  };
  tableModel.addRow(row);
  tableModel.fireTableDataChanged();
}
