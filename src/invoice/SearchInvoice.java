    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        index++;
         String invoiceIDText = txtInvoiID.getText();
         if(invoiceIDText.isEmpty()) {
             JOptionPane.showMessageDialog(this,
                     "You have to type number invoice id to search",
                     "Eror",JOptionPane.ERROR_MESSAGE);
             return;
         }
    try {
        // Chuyển đổi giá trị sang kiểu số nguyên
        int invoiceID = Integer.parseInt(invoiceIDText);
        // Gọi phương thức tìm kiếm với giá trị invoiceID
        var result = im.readInvoicesUsingPrepareStatement(invoiceID);
        if(result.getInvoice_id() ==0) {
           JOptionPane.showMessageDialog(null, "this invoice doesn't existed!");
           txtInvoiID.setText("");
           return;
        }
        showInvoice(result);

        // Xóa nội dung trong trường nhập liệu
        txtInvoiID.setText("");
    } catch (NumberFormatException e) {
        // Xử lý lỗi khi giá trị nhập vào không phải là số nguyên
        JOptionPane.showMessageDialog(this, "ID of invoid is a interger", "Eror", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSearchActionPerformed

