    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
       
            if(id.isEmpty()){
                jLabel2.setVisible(true);}  
        int employee_id = Integer.parseInt(id);
            Employee employee = Employee_Management.getEmployeeById(employee_id);
         if( employee==null){                       
                         JOptionPane.showMessageDialog(this, "Employee not existed", "Error", 2);
                        txtEmployeeID.setText("");     
         }
         else{
         txtFullName.setText(employee.getFull_name());
         txtUsername.setText(employee.getUsername());
         txtPassword.setText(employee.getPassword());
         cbRole.setSelectedItem(employee.getRole());
         txtEmail.setText(employee.getEmail());
         txtAddress.setText(employee.getAddress());
         txtPhoneNumber.setText(employee.getPhone_number());
         txtJoinDate.setText(employee.getJoin_date().toString());
         txtSalary.setText(Double.toString(employee.getSalary()));
         rbIsActive.setSelected(employee.isIs_active());
         }
      
    }//GEN-LAST:event_btnBrowseActionPerformed

