    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        int employee_id = Integer.parseInt(id);
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String role = cbRole.getSelectedItem().toString();
        String email = txtEmail.getText();
        String phone_number = txtPhoneNumber.getText();
        String address = txtAddress.getText();
        String salary1 = txtSalary.getText();
         var salary = Double.parseDouble(salary1);
        String is_active1 = rbIsActive.getText();
        var is_active = Boolean.parseBoolean(is_active1);
        Employee_Management.updateDataUsingStatement(employee_id, username, password, role, email, phone_number,
                address, salary, is_active);
         JOptionPane.showMessageDialog(null, "Thực hiện thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnConfirmActionPerformed

