public static int idCustomer(String username) {
         try (var conn = dsConnectDatabase.dataSource.getConnection()){
            int x = -1;
            Statement st = conn.createStatement();
            String sql = "select customer_id from customer where username = '" + username + "'";
            ResultSet rs = st.executeQuery(sql);  
            while (rs.next()) {
                x = rs.getInt("customer_id");
            }
            return x;
        } catch (Exception e) {
            return -1;
        }
    }


    
