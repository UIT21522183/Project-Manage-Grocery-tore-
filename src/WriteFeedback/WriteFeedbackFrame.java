   private void LayDuLieu() {
    String SQL = "SELECT feedback_id, customer_id, product_id, feedback_content FROM dbo.Feedback";
   ResultSet rs= null;
    try (var conn = dsConnectDatabase.dataSource.getConnection()){
        Statement st = conn.createStatement();
             rs = st.executeQuery(SQL);
        Object[] obj = new Object[]{"feedback_id", "customer_id", "product_id", "feedback_content"};
        DefaultTableModel tableModel = new DefaultTableModel(obj, 0);
        tblSanPham.setModel(tableModel);
        while (rs.next()) {
            Object[] item = new Object[4];
            item[0] = rs.getInt("feedback_id");
            item[1] = rs.getInt("customer_id");
            item[2] = rs.getInt("product_id");
            item[3] = rs.getString("feedback_content");
            tableModel.addRow(item);
        }
    } catch (SQLException e) {
        e.printStackTrace();
    } finally {
        if (rs != null) {
            try {
                rs.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
}
   
   

   
    
